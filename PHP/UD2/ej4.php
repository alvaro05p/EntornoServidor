<?php

    $num1 = readline("Numero 1: ");
    $num2 = readline("Numero 2: ");

    $suma = $num1 + $num2;

    $resta = $num1 - $num2;

    $multi = $num1 * $num2;

    $divi = $num1 / $num2;

    echo $num1 . "+" . $num2 . "=" . $suma . "\n";

    echo $num1 . "-" . $num2 . "=" . $resta . "\n";

    echo $num1 . "*" . $num2 . "=" . $multi . "\n";

    echo $num1 . "/" . $num2 . "=" . $divi . "\n";

?>